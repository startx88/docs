/**
 * Docker Compose:
 * 1. It is a tool used to defining and running multi-container applications.
 * 2. With compose you use yaml file to configure your application's services.
 * 3. With a single command, you create and run all the services from you configuration.
 *
 * How to use:
 * Using compose is basically a three step process:
 * 1. Define your apps environment with a Dockerfile so it can be reproduces anywhere.
 * 2. Define the services that make up your app in docker-compose.yml so they can run together in an isolated environment.
 * 3. Run docker-compose down && docker-compose up --build
 *
 */

/**
 * Docker compose commands:
 * build       :    Build or rebuild services
 * convert     :    Converts the compose file to platform's canonical format
 * create      :    Creates containers for a service.
 * down        :    Stop and remove containers, networks
 * exec        :    Execute a command in a running container.
 * kill        :    Force stop service containers.
 * logs        :    View output from containers
 * ls          :    List running compose projects
 * pause       :    pause services
 * ps          :    List containers
 * pull        :    Pull service images
 * push        :    Push service images
 * rm          :    Removes stopped service containers
 * run         :    Run a one-off command on a service.
 * start       :    Start services
 * stop        :    Stop services
 * unpause     :    unpause services
 * up          :    Create and start containers
 *
 *
 * Flags:
 *     --env-file string       Specify an alternate environment file.
 * -f, --file stringArray      Compose configuration files
 * -h, --help                  help for compose
 *     --profile stringArray   Specify a profile to enable
 * -p, --project-name string   Project name
 *     --workdir string        Specify an alternate working directory
 */

